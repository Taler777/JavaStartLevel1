package Java1Lesson1;

public class Main3 {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;
        // арифметические операции
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        //  операции присвоения
        a+=b;
        a-=b;
        a*=b;
        a/=b;

        a++; //инкремент
        a--; // декремент
        System.out.println(a);
    }
}
